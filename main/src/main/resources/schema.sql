DROP TABLE if EXISTS compilation_events,compilations,requests,events,users,categories;

CREATE TABLE if NOT EXISTS users
(
    id BIGINT GENERATED ALWAYS AS IDENTITY (START WITH 0 INCREMENT BY 1 MINVALUE 0) PRIMARY KEY,
    name varchar(255) NOT NULL,
    email varchar(255) NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS categories (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 0 INCREMENT BY 1 MINVALUE 0) PRIMARY KEY,
    name VARCHAR(255) NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS events (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 0 INCREMENT BY 1 MINVALUE 0) PRIMARY KEY,
    title VARCHAR(256),
    annotation TEXT NOT NULL,
    category_id BIGINT REFERENCES categories(id) ON DELETE CASCADE NOT NULL,
    paid BOOLEAN NOT NULL,
    event_date TIMESTAMP WITHOUT TIME ZONE NOT NULL,
    initiator_id BIGINT REFERENCES users(id) ON DELETE CASCADE,
    description TEXT,
    participant_limit BIGINT NOT NULL,
    state VARCHAR(64) NOT NULL,
    created_on TIMESTAMP WITHOUT TIME ZONE NOT NULL,
    lat FLOAT NOT NULL,
    lon FLOAT NOT NULL,
    published_on TIMESTAMP WITHOUT TIME ZONE,
    moderation BOOLEAN,
    confirmed_requests BIGINT
);


CREATE TABLE IF NOT EXISTS requests (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 0 INCREMENT BY 1 MINVALUE 0) PRIMARY KEY,
    event_id   BIGINT REFERENCES events(id) ON DELETE CASCADE,
    requester_id    BIGINT REFERENCES users(id) ON DELETE CASCADE,
    status     VARCHAR(100)       NOT NULL,
    created    TIMESTAMP WITHOUT TIME ZONE NOT NULL
);

CREATE TABLE IF NOT EXISTS compilations (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 0 INCREMENT BY 1 MINVALUE 0) PRIMARY KEY,
    title VARCHAR(50) NOT NULL UNIQUE,
    pinned BOOLEAN
);

CREATE TABLE IF NOT EXISTS compilation_events (
    compilation_id BIGINT REFERENCES compilations (id) ON DELETE CASCADE,
    event_id       BIGINT REFERENCES events (id) ON DELETE CASCADE
);

